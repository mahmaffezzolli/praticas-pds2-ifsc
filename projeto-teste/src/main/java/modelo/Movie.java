package modelo;

public class Movie {

	private int idMovie;
	private String nomeMovie;
	
	public int getIdMovie() {
		return idMovie;
	}
	public void setIdMovie(int idMovie) {
		this.idMovie = idMovie;
	}
	public String getNomeMovie() {
		return nomeMovie;
	}
	public void setNomeMovie(String nomeMovie) {
		this.nomeMovie = nomeMovie;
	}
	 
	
}
